package com.model;

public class Course {
	public String teacherId;
	public String courseId;
	public String courseName;
	public String courseStartTime;
	public String courseIntroduction;
	public String courseLabel;
	public String courseImgPath;
	public String courseCategory;
	public String CourseReferences;
	public String getTeacherId() {
		return teacherId;
	}
	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseStartTime() {
		return courseStartTime;
	}
	public void setCourseStartTime(String courseStartTime) {
		this.courseStartTime = courseStartTime;
	}
	public String getCourseIntroduction() {
		return courseIntroduction;
	}
	public void setCourseIntroduction(String courseIntroduction) {
		this.courseIntroduction = courseIntroduction;
	}
	public String getCourseLabel() {
		return courseLabel;
	}
	public void setCourseLabel(String courseLabel) {
		this.courseLabel = courseLabel;
	}
	public String getCourseImgPath() {
		return courseImgPath;
	}
	public void setCourseImgPath(String courseImgPath) {
		this.courseImgPath = courseImgPath;
	}
	public String getCourseCategory() {
		return courseCategory;
	}
	public void setCourseCategory(String courseCategory) {
		this.courseCategory = courseCategory;
	}
	public String getCourseReferences() {
		return CourseReferences;
	}
	public void setCourseReferences(String courseReferences) {
		CourseReferences = courseReferences;
	}
}
